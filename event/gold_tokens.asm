CountFoundGoldTokens::
	; returns the amount of gold tokens found in de, irrespective of the amount currently in hand
	ld hl, FourGoldTokenEvents
	call CountEventFlagsFromList
	push de
	ld hl, GoldTokenEvents
	call CountEventFlagsFromList
	pop hl
	add hl, hl
	add hl, hl
	add hl, de
	ld d, h
	ld e, l
	ret

